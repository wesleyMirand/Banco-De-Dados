                                              Diagrama Entidade Relacional
                                              
                                              
                            Um grande exemplo que temos, é quando recebe mensagem de erro ao tentar
                            usar, um nome de usúario. Isso ocorre porque, o nome escolhido ja está
                            em uso. E os comercio que controlam umonte de pedidos, clientes, cartão.
                            As resposta estão no banco de dados, coleção de informações organizadas,
                            que o dados possa ser gerenciado usado, atualizado e recuperado. E para 
                            entender como esses dados relacionam entre si, os engenheiros de dados
                            precisam, de um auxilio visual para entender, como cada um desses dados
                            estão relacionado e entender como eles funcionam junto e para mostrar
                            isso. Foi criado o "DIAGRAMA ENTIDADE-RELACIONAL".
                            
                            
                            
